    private void jCalcTipButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCalcTipButtonActionPerformed
	String newValue = this.jFormattedTextField1.getText();
	if (newValue != null) {
	    tipCalc.setCustomValueFromString(newValue);
	}
	tipAmount = tipCalc.calculateTip();
	jTipAmount.setText(Double.toString(tipAmount));
    }//GEN-LAST:event_jCalcTipButtonActionPerformed

    private void jServiceQualityChoicesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jServiceQualityChoicesActionPerformed
	JComboBox cb = (JComboBox) evt.getSource();
	if (cb != null) {
	    ServiceQuality serviceQuality = (ServiceQuality) cb.getSelectedItem();
	    if (serviceQuality != null) {
		tipCalc.setServiceQuality(serviceQuality);
		tipAmount = tipCalc.calculateTip();
		jTipAmount.setText(Double.toString(tipAmount));
	    }
	}
    }//GEN-LAST:event_jServiceQualityChoicesActionPerformed

