    private void jTipCalcChoicesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTipCalcChoicesActionPerformed
	JComboBox cb = (JComboBox) evt.getSource();
	if (cb != null) {
	    String tipCalculatorName = (String) cb.getSelectedItem();
	    if (tipCalculatorName != null) {
		TipCalculator oldTipCalc = tipCalc;
		tipCalc = tipChoices.get(tipCalculatorName);
		if (tipCalc != null) {
		    this.jLabel3.setText(tipCalc.getPromptString());
		    this.jFormattedTextField1.setText(tipCalc.getCustomValueAsString());
		    tipAmount = tipCalc.calculateTip();
		    jTipAmount.setText(Double.toString(tipAmount));
		} else {
		    tipCalc = oldTipCalc;
		}
	    }
	}
    }//GEN-LAST:event_jTipCalcChoicesActionPerformed

